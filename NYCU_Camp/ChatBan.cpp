typedef long long ll;
const int maxn = 1e5 + 5;

ll sq_sum(ll x)
{
    return x * (x + 1) * (2 * x + 1) / 6;
}

void solve()
{
    ll x;
    cin >> x;
    ll idx = 0, s = 1ll<<20;
    while(s)
    {
        if(sq_sum(idx + s) < x)
            idx += s;
        s >>= 1;
    }
    cout << idx + 1 << '\n';
}

