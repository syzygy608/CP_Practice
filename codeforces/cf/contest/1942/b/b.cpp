void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for(auto &x: a)
        cin >> x;
    vector<int> b;
    int mex = n;
    for(int i = n - 1; i >= 0; --i)
    {
        if(a[i] == mex - i)
        {
            b.push_back(i);
            if(i < mex)
                mex = i;
        }
        else
        {
            b.push_back(mex - a[i]);
            if(mex - a[i] < mex)
                mex -= a[i];
        }
    }
    reverse(all(b));
    for(auto x: b)
        cout << x << ' ';
    cout << '\n';
}

int main()
{
    IO;
    int t = 1;
    cin >> t;
    while(t--)
        solve();
    return 0;
}
