typedef long long ll;
const int maxn = 1e5 + 5;

ll cal(ll r)
{
    ll ret = 0;
    for(ll i = 1; i <= r; i++)
    {
        ll t = floor(sqrt(r * r - i * i - 1)) + 1;
        ret += t;
    }
    ret *= 4;
    return ret;
}

void solve()
{
    ll r;
    cin >> r;
    cout << cal(r + 1) - cal(r)  << '\n';
}

